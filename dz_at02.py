def count_vowels(s: str) -> int:
    """
    Функция для подсчета количества гласных в строке.
    s: строка, в которой нужно подсчитать гласные
    return: количество гласных в строке
    """
    vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯaeiouyAEIOUY"  # Определяем гласные буквы
    count = 0  # Инициализируем счетчик гласных
    for char in s:  # Проходим по каждому символу в строке
        if char in vowels:  # Если символ является гласной
            count += 1  # Увеличиваем счетчик
    return count  # Возвращаем общее количество гласных
